ALTER VIEW [dbo].[viewnit]
AS
SELECT        dbo.MAESTRO_COMERCIOS.CODIGOUNICO, dbo.MAESTRO_COMERCIOS.NOMBRECOMERCIO, dbo.MAESTRO_COMERCIOS.RAZONSOCIAL, dbo.TERMINALES_REGISTRADAS.IDTERMINAL, 
