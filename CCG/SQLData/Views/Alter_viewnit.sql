                         dbo.TERMINALES_REGISTRADAS ON dbo.MAESTRO_COMERCIOS.CODIGOUNICO = dbo.TERMINALES_REGISTRADAS.CODIGOUNICO INNER JOIN
                         dbo.NIT ON dbo.TERMINALES_REGISTRADAS.IDTERMINAL = dbo.NIT.IDNIT
GO

